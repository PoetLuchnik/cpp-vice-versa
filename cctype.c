int cvv_isalnum(int c) {
	return cvv_cctype_table[c] & 0b1;
}

int cvv_isalpha(int c) {
	return cvv_cctype_table[c] & 0b10;
}

int cvv_isblank(int c) {
	return cvv_cctype_table[c] & 0b100;
}

int cvv_iscntrl(int c) {
	return cvv_cctype_table[c] & 0b1000;
}

int cvv_isdigit(int c) {
	return cvv_cctype_table[c] & 0b10000;
}

int cvv_isgraph(int c) {
	return cvv_cctype_table[c] & 0b100000;
}

int cvv_islower(int c) {
	return cvv_cctype_table[c] & 0b1000000;
}

int cvv_isprint(int c) {
	return cvv_cctype_table[c] & 0b10000000;
}

int cvv_ispunct(int c) {
	return cvv_cctype_table[c] & 0b100000000;
}

int cvv_isspace(int c) {
	return cvv_cctype_table[c] & 0b1000000000;
}

int cvv_isupper(int c) {
	return cvv_cctype_table[c] & 0b10000000000;
}

int cvv_isxdigit(int c) {
	return cvv_cctype_table[c] & 0b100000000000;
}

int cvv_tolower(int c) {
	return c + (cvv_isupper(c) ? ('a' - 'A') : 0);
}

int cvv_toupper(int c) {
	return c - (cvv_islower(c) ? ('a' - 'A') : 0);
}